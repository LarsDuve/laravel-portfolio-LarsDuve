            <h1>My Motivation</h1>
            <h3>1. Theory</h3>
            <p>
                What is motivation?<br>
                Motivation: A reason or reasons for acting or behaving in a particular way.<br>
                Most of the time there is spoken of two types of motivation. There is extrinsic motivation and intrinsic motivation. Extrinsic motivation is driven by external forces such as money or praise while intrinsic motivation is something that comes from within and can be as simple as the joy one feels after accomplishing a challenging task.
                Besides these 2 types of motivation there are 2 more types, identified and introjected motivation. These types of motivation are non-action which means that they do not involve physical actions but a more mental state for doing something.<br>
                <br>
                In the TED talk about motivation from Daniel Pink I made notes that summarize his lecture: <br>
                If you wanted to motivate people, it would make sense to reward them for performing better. Study has shown that it does not work this way. <br>
                Why would people perform worse with a bigger reward?<br>
                When people are offered a reward, they start to focus more. If you would need to come up with a new idea to solve a problem, you would not want them to focus but you would want them to think broadly.<br>
                So, for the best motivation it is important to make the rewards not the incentive and let people work how they want on something that really interests them.<br>
            </p><br>
            <h3>2. Performance motivation</h3>
